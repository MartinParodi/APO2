package util;

public class Double {
	
	private Double prev;
	private Double next;
	private Object Content;
	
	
	
	public Double getPrev() {
		return prev;
	}
	public void setPrev(Double prev) {
		this.prev = prev;
	}
	public Double getNext() {
		return next;
	}
	public void setNext(Double next) {
		this.next = next;
	}
	public Object getContent() {
		return Content;
	}
	public void setContent(Object content) {
		Content = content;
	}
	
	

}
